# 没有 /* */ 多行注释语法。多行通常这样写:
#   - 每行都加 #  (推荐)
#   - 或用三引号字符串做"伪多行注释" (技术上是字符串字面量)

"""
这是一个三引号字符串, 没被赋值或使用, 等于被忽略。
很多人会拿它当多行注释用, 但严格来说这不是注释。
真正的文档字符串(docstring)放在函数/类的开头, 是有特殊用途的。
"""


# ------------------------------------------------------------
# 六、`if __name__ == "__main__":` 这个怪异写法
# ------------------------------------------------------------
# 你会在所有 Python 项目里看到它。它的作用是:
#
#   「只有当这个文件被直接运行时, 才执行下面的代码;
#    如果这个文件被别的代码 import, 不执行。」
#
# __name__ 是 Python 自动给每个模块设置的特殊变量:
#   - 直接运行此文件:   __name__ == "__main__"
#   - 被其他文件 import: __name__ == "01_basics"  (文件名)
#
# 类比 Java: 想象 Java 允许任何 class 都有 main 方法,
# 但只有"被直接运行的那个 class"的 main 会被调用。
#
# 把"启动入口"放在 if 块里, 是为了让文件既能直接运行,
# 又能被别人 import 而不会意外执行启动代码。
# 约定俗成的最佳实践, 几乎所有 Python 文件都会有。


def greet(person: str) -> str:
    """
    一个简单的函数 —— 我们下一课会专门讲函数。
    `-> str` 表示这个函数返回 str 类型。
    """
    return f"你好, {person}！"


if __name__ == "__main__":
    # 只有直接运行此文件时才会进入这里。
    print()  # 打印一个空行
    print("=" * 40)  # "=" * 40 等于 "=" 重复 40 次。Python 字符串可以乘整数!
    print("以下是 __main__ 块的输出")
    print("=" * 40)

    message = greet("Java 程序员")
    print(message)

    # 遍历一个列表 (下一课会详细讲 list):
    for person in ["Alice", "Bob", "你自己"]:
        print(greet(person))


