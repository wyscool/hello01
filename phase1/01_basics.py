# ------------------------------------------------------------
# 一、Python 程序的"入口" —— 没有 main class
# ------------------------------------------------------------
# Java 必须把代码写在 class 里:
#   public class Hello {
#       public static void main(String[] args) {
#           System.out.println("hi");
#       }
#   }
#
# Python 是脚本式语言: 从文件最上面开始执行，一直到最后。
# 不需要 class，不需要 main 方法。
#
# 下面这一行是一个"顶层语句", 程序启动时立即运行:

print("Hello, Python!")  # print 是内置函数, 相当于 System.out.println


# ------------------------------------------------------------
# 二、变量赋值 —— 不需要声明类型
# ------------------------------------------------------------
# Java:  String name = "Alice";  必须先写类型
# Python: name = "Alice"          直接赋值, 类型自动推断

name = "Alice"     # str  (字符串, 由 "" 自动识别)
age = 30           # int  (整数)
height = 1.72      # float (浮点数, 有小数点)
is_student = True  # bool (注意: True/False 首字母大写!)
nothing = None     # NoneType (Python 的 "空", 相当于 Java 的 null)

# 重要: 上面没有出现 String/int/float/boolean 关键字!
# Python 通过你赋的值自动推断类型, 这叫"动态类型"。

# 用 type() 内置函数查看类型:
print(type(name))        # <class 'str'>
print(type(age))         # <class 'int'>
print(type(is_student))  # <class 'bool'>
print(type(nothing))     # <class 'NoneType'>
print(type(None))


# ------------------------------------------------------------
# 三、Type Hints —— 现代 Python 的最佳实践
# ------------------------------------------------------------
# 既然 Python 不需要声明类型, 为什么还要写类型?
# —— 给 IDE、类型检查器(mypy/pyright)、和团队成员看, 提高可读性和可维护性。
#
# 关键反直觉点: type hints **不在运行时强制类型**!
# 你写 int 然后赋字符串, 程序照样跑, 只是 IDE 会标红。

city: str = "Shanghai"   # 显式标注: 变量名 后面 : 类型 = 值
year: int = 2026
pi: float = 3.14159
admin: bool = False

# 注意区分: `:` 是类型标注, `=` 是赋值。不要混淆。

# 生产代码中, 公开函数的参数、返回值几乎都会写 type hints。
# 局部变量看情况, 简单的可以不写。本课程后面的代码会逐步加上, 你慢慢熟悉。


# ------------------------------------------------------------
# 四、打印与 f-string (格式化字符串)
# ------------------------------------------------------------
# Java 拼字符串通常: "name=" + name + ", age=" + age
# 或   String.format("name=%s, age=%d", name, age)
#
# Python 推荐 f-string: 字符串前加 f, {} 里写表达式。

print(f"name={name}, age={age}")

# f-string 里可以放任何 Python 表达式, 不只是变量:
print(f"明年 {name} 就 {age + 1} 岁了")

# 格式化数字:
print(f"pi 保留两位小数: {pi:.2f}")    # 3.14
print(f"age 占 5 位右对齐: '{age:>5}'") # '   30'

# 这是 2026 年最主流的字符串格式化方式, 老代码里你可能还会见到:
#   "%s %d" % (name, age)        — 老式, 不推荐
#   "{} {}".format(name, age)    — 中等老, 偶尔见到
#   f"{name} {age}"              — 现代, 优先用这个


